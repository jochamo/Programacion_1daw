package ud4_estructurasDeControl;

public class Ejercicio10 {

	public static void main(String[] args) {

		int n = 8;

		//******************************
		int i = 3;
		while (i<=n) {
			System.out.println(i);
			i = i+3;
		}
		//******************************
		i = 0;
		while (i<n) {
			i = i+3;
			System.out.println(i);
		}
		//******************************
	}

}